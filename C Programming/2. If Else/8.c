// Find Minimum and Maximum of Three Numbers

#include <stdio.h>

int main() {
    int a, b, c;
    printf("Enter three numbers: ");
    scanf("%d %d %d", &a, &b, &c);

    int max = (a > b) ? ((a > c) ? a : c) : ((b > c) ? b : c);
    int min = (a < b) ? ((a < c) ? a : c) : ((b < c) ? b : c);

    printf("Maximum: %d\n", max);
    printf("Minimum: %d\n", min);
    return 0;
}
